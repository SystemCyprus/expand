    <h1>L'objet d'erreur JavaScript</h1>
    <p>JavaScript réserve un objet d'erreur qui fournit des informations sur l'erreur lorsqu'une erreur se produit.</p>
    <p>L'objet error fournit deux propriétés utiles: <code>name</code> et <code>message</code>.</p>
    <p><code>name</code>: définit ou renvoie un nom d'erreur</p>
    <p><code>message</code>: envoie un message d'erreur (une chaîne)</p>

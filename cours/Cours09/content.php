<section id="DOM" class="contenuChapitre">
    <h1>Le modèle objet document DOM (Document Object Model)</h1>
    <img alt="image du DOM non trouvée" src="dom.gif" width="100%" height="100%" />
    <p>Avec le DOM HTML, JavaScript peut accéder et modifier tous les éléments d'un document HTML.</p>
    <p>Lorsqu'une page Web est chargée, le navigateur crée un modèle objet de document de la page.</p>
    <p>Dans le DOM, tous les éléments HTML sont définis comme des objets.</p>
    <p>L'interface de programmation représente les propriétés et les méthodes de chaque objet.</p>
    <p>Une propriété est une valeur que vous pouvez obtenir ou définir (comme changer le contenu d'un élément HTML).</p>
    <p>Une méthode est une action que vous pouvez effectuer (comme ajouter ou supprimer un élément HTML.</p>
    <p>L'exemple suivant modifie le contenu (le <code>innerHTML</code>) de l'élément avec <code>id="1"</code>:</p>
    <p class="codepen" data-height="186" data-theme-id="dark" data-default-tab="js" data-slug-hash="gORwoev" data-user="c64ren" style="height: 186px; box-sizing: border-box; display: flex; align-items: center; justify-content: center; border: 2px solid; margin: 1em 0; padding: 1em;">
        <span>See the Pen <a href="https://codepen.io/c64ren/pen/gORwoev">
                DOM</a> by c64ren (<a href="https://codepen.io/c64ren">@c64ren</a>)
            on <a href="https://codepen.io">CodePen</a>.</span>
    </p>
    <p>
        La propriété <code>innerHTML</code> peut être utilisée pour obtenir ou modifier n'importe quel élément <code>HTML</code>, y compris <code>&lt;html&gt;</code> et <code>&lt;body&gt;</code>.
    </p>
</section>


<section id="L'attribut formmethod" class="contenuChapitre">
    <h1>L'attribut <code>formmethod</code></h1>
    <p>L'attribut <code>formmethod</code> définit la méthode HTTP pour l'envoi de données de formulaire à l'URL d'action. Cet attribut remplace l'attribut method de l'élément <code>form</code></p>
    <p>Les données de formulaire peuvent être envoyées sous forme de variables URL (<code>method="get"</code>) ou sous forme de transaction HTTP post (<code>method="post"</code>)</p>
    <h2>Méthode <code>get</code></h2>
    <p>Cette méthode ajoute les données de formulaire à l'URL dans des paires nom/valeur</p>
    <p>Elle est utile pour l’envoi de formulaires où un utilisateur souhaite marquer le résultat</p>
    <p>N'utilisez jamais la méthode <code>get</code> pour transmettre des informations sensibles</p>
    <p>Exemple: le mot de passe ou d'autres informations sensibles seront visibles dans la barre d'adresse du navigateur</p>
    <h2>Méthode <code>post</code></h2>
    <p>Cette méthode envoie les données du formulaire en tant que transaction HTTP post</p>
    <p>La méthode <code>post</code> est plus robuste et sécurisée que <code>get</code>, et n'a pas de limitations de taille</p>
